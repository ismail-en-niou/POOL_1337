int	ft_iterative_factorial(int nb)
{
	int		i;
	int		res;

	if (nb < 0)
		return (0);
	i = nb;
	res = 1 ;
	while (i > 1)
	{
		res = res * (i);
		i--;
	}
	return (res);
}
